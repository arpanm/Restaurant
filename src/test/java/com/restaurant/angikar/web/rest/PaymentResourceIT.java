    @Test
    @Transactional
    void putExistingPayment() throws Exception {
        // Initialize the database
        paymentRepository.saveAndFlush(payment);

        int databaseSizeBeforeUpdate = paymentRepository.findAll().size();

        // Update the payment
        Payment updatedPayment = paymentRepository.findById(payment.getId()).orElseThrow();
        // Disconnect from session so that the updates on updatedPayment are not directly saved in db
        em.detach(updatedPayment);
        updatedPayment
            .status(UPDATED_STATUS)
            .vendor(UPDATED_VENDOR)
            .type(UPDATED_TYPE)
            .amount(UPDATED_AMOUNT)
            .initDate(UPDATED_INIT_DATE)
            .updatedDate(UPDATED_UPDATED_DATE)
            .totalRefundAmount(UPDATED_TOTAL_REFUND_AMOUNT);
        PaymentDTO paymentDTO = paymentMapper.toDto(updatedPayment);

        restPaymentMockMvc
            .perform(
                put(ENTITY_API_URL_ID, paymentDTO.getId())
                    .contentType(MediaType.APPLICATION_JSON)
                    .content(TestUtil.convertObjectToJsonBytes(paymentDTO))
            )
            .andExpect(status().isOk());

        // Validate the Payment in the database
        List<Payment> paymentList = paymentRepository.findAll();
        assertThat(paymentList).hasSize(databaseSizeBeforeUpdate);
        Payment testPayment = paymentList.get(paymentList.size() - 1);
        assertThat(testPayment.getStatus()).isEqualTo(UPDATED_STATUS);
        assertThat(testPayment.getVendor()).isEqualTo(UPDATED_VENDOR);
        assertThat(testPayment.getType()).isEqualTo(UPDATED_TYPE);
        assertThat(testPayment.getAmount()).isEqualTo(UPDATED_AMOUNT);
        assertThat(testPayment.getInitDate()).isEqualTo(UPDATED_INIT_DATE);
        assertThat(testPayment.getUpdatedDate()).isEqualTo(UPDATED_UPDATED_DATE);
        assertThat(testPayment.getTotalRefundAmount()).isEqualTo(UPDATED_TOTAL_REFUND_AMOUNT);
    }

